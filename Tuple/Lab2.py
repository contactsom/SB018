mytuple=("Aashi")
print(mytuple)
print(type(mytuple)) # <class 'str'>

mytuple1=("Aashi",)
print(mytuple1)
print(type(mytuple1)) # <class 'tuple'>


myset={"Aashi"}
print(myset)
print(type(myset)) # <class 'set'>

mysList=["Aashi"]
print(mysList)
print(type(mysList)) # <class 'list'>
