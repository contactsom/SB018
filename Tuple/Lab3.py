mytuple=("Aashi")
print(mytuple)
print(type(mytuple)) # <class 'str'>

mytuple1="Aashi"
print(mytuple1)
print(type(mytuple1)) # <class 'str'>


mytuple2="Aashi",
print(mytuple2)
print(type(mytuple2)) # <class 'tuple'>


mytuple5=12345,
print(mytuple5)
print(type(mytuple5)) # <class 'tuple'>


mytuple5="Aashi","Vaizhnavi","Vikash","Gaurav","subrata"
print(mytuple5)
print(type(mytuple5)) # <class 'tuple'>
# Tuple
# () is optional
